package tasty.mushrooms.commons.exception;

public class TeemoInstantiationException extends Exception {
	private static final long serialVersionUID = -4709984926100943200L;

	public TeemoInstantiationException() {
		super();
	}

	public TeemoInstantiationException(String message, Throwable cause) {
		super(message, cause);
	}

	public TeemoInstantiationException(String message) {
		super(message);
	}

	public TeemoInstantiationException(Throwable cause) {
		super(cause);
	}

}
